#include <unistd.h>

void	write_car(int a, int b)
{
	char	c[7];

	c[2] = *" ";
	c[5] = *",";
	c[6] = *" ";
	c[1] = a % 10 + '0';
	c[4] = b % 10 + '0';
	c[0] = a / 10 + '0';
	c[3] = b / 10 + '0';
	if (a == 98)
	{
		write(1, &c, 5);
	}
	else
	{
		write(1, &c, 7);
	}	
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			if (i < j)
			{
				write_car(i, j);
			}
			j++;
		}
		i++;
	}
}
